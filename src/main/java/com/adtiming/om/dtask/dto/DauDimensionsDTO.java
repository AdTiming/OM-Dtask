public class DauDimensionsDTO {
    private Integer app;
    private Integer adn;
    private Integer instance;
    private Integer adn_placement;
    private Integer placement;

    public Integer getApp() {
        return app;
    }

    public void setApp(Integer app) {
        this.app = app;
    }

    public Integer getAdn() {
        return adn;
    }

    public void setAdn(Integer adn) {
        this.adn = adn;
    }

    public Integer getInstance() {
        return instance;
    }

    public void setInstance(Integer instance) {
        this.instance = instance;
    }

    public Integer getAdn_placement() {
        return adn_placement;
    }

    public void setAdn_placement(Integer adn_placement) {
        this.adn_placement = adn_placement;
    }

    public Integer getPlacement() {
        return placement;
    }

    public void setPlacement(Integer placement) {
        this.placement = placement;
    }
}
